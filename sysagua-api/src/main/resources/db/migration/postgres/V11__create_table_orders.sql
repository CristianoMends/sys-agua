CREATE TABLE IF NOT EXISTS orders (
    id BIGINT NOT NULL NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    customer_id BIGINT,
    delivery_person_id BIGINT,
    delivery_status VARCHAR(50),
    payment_status VARCHAR(50),
    paid_amount NUMERIC(10,2),
    total NUMERIC(10,2),
    balance NUMERIC(10,2),
    payment_method VARCHAR(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    finished_at TIMESTAMP,
    canceled_at TIMESTAMP,
    description VARCHAR(255),
    CONSTRAINT orders_pkey PRIMARY KEY (id),
    CONSTRAINT fk_orders_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_orders_delivery_person FOREIGN KEY (delivery_person_id) REFERENCES delivery_persons(id)
);
